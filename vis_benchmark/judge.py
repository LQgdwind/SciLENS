import json
import os
import sys
import time
import re
from typing import List, Dict, Tuple
from concurrent.futures import ThreadPoolExecutor, as_completed
from threading import Lock

from openai import OpenAI


def normalize_question(q: str) -> str:
    if not isinstance(q, str):
        return ""
    q = q.strip()
    return " ".join(q.split())


class ThreeMetricVisJudge:
    def __init__(self,
                 api_key: str,
                 model: str = "Qwen/Qwen3-30B-A3B",
                 max_workers: int = 32):
        self.client = OpenAI(
            api_key=api_key,
            base_url="http://localhost:8001/v1",
            max_retries=10
        )
        self.model = model
        self.max_workers = max_workers
        self.progress_lock = Lock()

    @staticmethod
    def _load_metrics_map(rubric_file: str) -> Dict[str, Dict]:
        q2metrics: Dict[str, Dict] = {}
        if not os.path.exists(rubric_file):
            print(f"[Error] Rubric file not found: {rubric_file}")
            return {}

        with open(rubric_file, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                try:
                    obj = json.loads(line)
                except Exception:
                    continue

                if "qas" in obj and isinstance(obj["qas"], list) and obj["qas"]:
                    inner = obj["qas"][0]
                else:
                    inner = obj

                qa = inner.get("qa", inner)
                question = qa.get("question", "")
                norm_q = normalize_question(question)
                if not norm_q:
                    continue

                metrics = inner.get("metrics", obj.get("metrics"))
                if not isinstance(metrics, dict):
                    continue

                q2metrics[norm_q] = metrics

        return q2metrics

    def _extract_model_answer(self, traj: Dict) -> str:
        ans = traj.get("model_final_answer")
        if ans and isinstance(ans, str) and ans.strip():
            return ans.strip()
        
        ans = traj.get("model_answer")
        if ans and isinstance(ans, str) and ans.strip():
            return ans.strip()

        convs = traj.get("conversations", [])
        if not convs or not isinstance(convs, list):
            return ""

        for msg in reversed(convs):
            if msg.get("role") == "assistant":
                content = str(msg.get("content", "") or "").strip()
                thinking = str(msg.get("thinking", "") or "").strip()
                
                if not content and not thinking:
                    continue

                full_text = f"{content}\n{thinking}"

                match = re.search(r"<answer>(.*?)</answer>", full_text, re.DOTALL | re.IGNORECASE)
                if match:
                    return match.group(1).strip()
                
                if content:
                    return content
                
                if thinking:
                    return thinking

        return ""

    def _construct_prompt(self, traj: Dict, metrics: Dict) -> List[Dict]:
        question = traj.get("question", "")
        gold_answer = traj.get("gold_answer", "")
        
        model_answer = self._extract_model_answer(traj)
        
        if not model_answer:
            model_answer = "[No Answer Generated by Model]"

        metrics_str = json.dumps(metrics, ensure_ascii=False, indent=2)

        system_prompt = """
You are an expert judge evaluating retrieval-augmented QA performance under a three-metric rubric.

Setting:
- The QUESTION describes an information need, often requiring scientific fact-checking or complex reasoning.
- The GOLD ANSWER provides the correct reference facts, entities, and scientific conclusions.
- The MODEL FINAL ANSWER contains the model's generated response and logical reasoning.

The three metrics are:

1) ASF (Answer Semantic Fidelity)
   - Semantic fidelity of the model's final answer w.r.t. the reference (gold) answer:
     key entities, numbers, relations, and conclusions.
   - Focus on whether the answer captures the same core facts and meaning as the reference,
     allowing reasonable paraphrases but strictly penalizing missing or contradictory core information.

2) QIS (Query Intent Satisfaction)
   - How well the model's reasoning and answer satisfy the information need
     expressed in the question.
   - Focus on whether the model understands what is being asked (e.g., value/definition/comparison/cause),
     respects the requested scope (specific paper, time range, entity, etc.),
     and avoids drifting into irrelevant academic aspects.

3) EGQ (Evidence Grounding Quality)
   - How well the answer and reasoning are grounded in the kind of evidence implied
     by the setting (supporting statements, citations, retrieval context).
   - Since you do not see the raw retrieval here, judge EGQ by:
       (a) Whether the answer is consistent and precise enough that it could realistically
           be supported by the described statements/citations in the rubric descriptions;
       (b) Whether the answer avoids hallucinated claims or variables that clearly contradict
           or go beyond what would be supported.

You are given a metrics schema with:
- For each metric in {ASF, QIS, EGQ}, a list of rubrics (criteria) with weights that
  sum to 1 within that metric.

Your job:
- For EACH rubric r under EACH metric M, decide whether the MODEL FINAL ANSWER
  satisfies that rubric (PASS) or does not satisfy it (FAIL).
- You MUST NOT compute any numeric scores or overall scores yourself.
- ONLY return pass/fail judgments at the rubric level.
""".strip()

        user_parts = []
        user_parts.append("[Question]\n")
        user_parts.append(question)
        user_parts.append("\n\n[Reference (Gold) Answer]\n")
        user_parts.append(gold_answer)
        user_parts.append("\n\n[Model Final Answer]\n")
        user_parts.append(model_answer)
        user_parts.append("\n\n[Metrics Schema]\n")
        user_parts.append(metrics_str)
        user_parts.append(
            "\n\nTask:\n"
            "For EACH metric (use the exact metric names given in the schema), and for EACH rubric under that metric,\n"
            "decide whether the MODEL FINAL ANSWER satisfies that rubric.\n"
            "- If it satisfies the rubric, set pass = true.\n"
            "- If it does not satisfy the rubric, set pass = false.\n\n"
            "Return ONLY valid JSON, no markdown, no backticks, with the following structure:\n\n"
            "{\n"
            '  "rubric_pass": {\n'
            '    "<METRIC_NAME>": [\n'
            '      {"description": "<rubric description copied exactly>", "pass": true},\n'
            "      ...\n"
            "    ],\n"
            "    ... (for other metrics)\n"
            "  }\n"
            "}\n"
        )
        user_prompt = "".join(user_parts)

        return [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt},
        ]

    def _process_item(self,
                      item_data: Tuple[int, str],
                      q2metrics: Dict[str, Dict]) -> Tuple[int, Dict, bool]:
        idx, line = item_data
        line = line.strip()
        if not line:
            return idx, None, False

        try:
            traj = json.loads(line)
        except Exception as e:
            return idx, {"_error": True, "_error_msg": str(e)}, False

        q = traj.get("question", "")
        norm_q = normalize_question(q)
        metrics_cfg = q2metrics.get(norm_q)
        if metrics_cfg is None:
            traj["judge_scores"] = {
                "metric_scores": {},
                "overall_score": None,
                "rubric_pass": {},
                "_error": "no_metrics_for_question"
            }
            return idx, traj, False

        metric_names = list(metrics_cfg.keys())

        max_retries = 3
        for attempt in range(max_retries):
            try:
                messages = self._construct_prompt(traj, metrics_cfg)
                resp = self.client.chat.completions.create(
                    model=self.model,
                    messages=messages,
                    response_format={"type": "json_object"},
                    temperature=0.2 if attempt > 0 else 0.0
                )
                content = resp.choices[0].message.content
                if not content or not content.strip():
                    traj["judge_scores"] = {
                        "metric_scores": {},
                        "overall_score": None,
                        "rubric_pass": {}
                    }
                    return idx, traj, False

                txt = content.strip()
                if txt.startswith("```json"):
                    txt = txt[7:]
                elif txt.startswith("```"):
                    txt = txt[3:]
                if txt.endswith("```"):
                    txt = txt[:-3]
                txt = txt.strip()

                out = json.loads(txt)
                rubric_pass = out.get("rubric_pass", {})

                cleaned_rubric_pass: Dict[str, List[Dict]] = {}
                for m in metric_names:
                    rp_list = rubric_pass.get(m, [])
                    cleaned_list = []
                    if isinstance(rp_list, list):
                        for r in rp_list:
                            if not isinstance(r, dict):
                                continue
                            desc = str(r.get("description", "")).strip()
                            if not desc:
                                continue
                            ps = r.get("pass", False)
                            ps_bool = bool(ps)
                            cleaned_list.append({
                                "description": desc,
                                "pass": ps_bool
                            })
                    cleaned_rubric_pass[m] = cleaned_list

                metric_scores: Dict[str, float] = {}

                def to_float(x, default=0.0):
                    try:
                        return float(x)
                    except Exception:
                        return default

                for m in metric_names:
                    metric_def = metrics_cfg.get(m, {})
                    rubrics_def = metric_def.get("rubrics", [])
                    passes = {r["description"]: r["pass"]
                              for r in cleaned_rubric_pass.get(m, [])}
                    s_m = 0.0
                    for cfg in rubrics_def:
                        desc = str(cfg.get("description", "")).strip()
                        w = to_float(cfg.get("weight", 0.0), 0.0)
                        if not desc:
                            continue
                        if passes.get(desc, False):
                            s_m += w
                    metric_scores[m] = s_m

                overall = 0.0
                for m in metric_names:
                    W_m = to_float(metrics_cfg.get(m, {}).get("weight", 0.0), 0.0)
                    overall += W_m * metric_scores[m]

                traj["judge_scores"] = {
                    "rubric_pass": cleaned_rubric_pass,
                    "metric_scores": metric_scores,
                    "overall_score": overall
                }

                return idx, traj, True

            except Exception as e:
                if attempt < max_retries - 1:
                    time.sleep(1 * (attempt + 1))
                    continue
                traj["judge_scores"] = {
                    "metric_scores": {},
                    "overall_score": None,
                    "rubric_pass": {},
                    "_error": str(e),
                    "_retry_exhausted": True
                }
                return idx, traj, False

    def _print_progress(self, completed: int, total: int, errors: int, start_time: float):
        elapsed = time.time() - start_time
        progress = completed / total if total > 0 else 0
        bar_width = 40
        filled = int(bar_width * progress)
        bar = "█" * filled + "░" * (bar_width - filled)

        rate = completed / elapsed if elapsed > 0 else 0
        eta = (total - completed) / rate if rate > 0 else 0

        elapsed_str = f"{int(elapsed // 60)}m{int(elapsed % 60)}s"
        eta_str = f"{int(eta // 60)}m{int(eta % 60)}s" if eta < 3600 else f"{int(eta / 3600)}h"

        sys.stdout.write(
            f"\r|{bar}| {progress*100:5.1f}% ({completed}/{total}) "
            f"[{elapsed_str}<{eta_str}, {rate:.1f}it/s, errors:{errors}]"
        )
        sys.stdout.flush()

    def process_file(self,
                     traj_file: str,
                     rubric_file: str,
                     output_file: str):
        print(f"Loading visualization metrics from {rubric_file}...")
        q2metrics = self._load_metrics_map(rubric_file)
        if not q2metrics:
            print("No metrics loaded. Skipping file.")
            return

        print(f"Reading trajectories from {traj_file}...")
        if not os.path.exists(traj_file):
            print(f"Error: Input file {traj_file} does not exist.")
            return

        with open(traj_file, "r", encoding="utf-8") as f:
            lines = f.readlines()
        total = len(lines)
        print(f"Total trajectories: {total}. Starting judging with {self.max_workers} workers...")

        indexed_lines = list(enumerate(lines))
        results: List[Dict] = [None] * total
        completed = 0
        errors = 0
        start_time = time.time()

        with ThreadPoolExecutor(max_workers=self.max_workers) as executor:
            future_to_idx = {
                executor.submit(self._process_item, data, q2metrics): data[0]
                for data in indexed_lines
            }

            for future in as_completed(future_to_idx):
                idx, item, success = future.result()
                results[idx] = item
                with self.progress_lock:
                    completed += 1
                    if not success:
                        errors += 1
                    if completed % 10 == 0 or completed == total:
                        self._print_progress(completed, total, errors, start_time)

        print()
        print(f"Writing judged trajectories to {output_file}...")
        
        os.makedirs(os.path.dirname(os.path.abspath(output_file)), exist_ok=True)
        
        with open(output_file, "w", encoding="utf-8") as f_out:
            for item in results:
                if item is not None:
                    f_out.write(json.dumps(item, ensure_ascii=False) + "\n")

        elapsed = time.time() - start_time
        print(
            f"Done. Processed {completed} trajectories in {elapsed:.1f}s "
            f"({errors} errors). Output saved to {output_file}"
        )

        metric_names: List[str] = []
        for item in results:
            if item and "judge_scores" in item:
                metric_names = list(item["judge_scores"].get("metric_scores", {}).keys())
                break

        if not metric_names:
            print("[SUMMARY] No metric_scores to average.")
            return

        sum_metric = {m: 0.0 for m in metric_names}
        sum_overall = 0.0
        n_valid = 0

        for item in results:
            if not item:
                continue
            js = item.get("judge_scores", {})
            metric_scores = js.get("metric_scores", {})
            overall = js.get("overall_score", None)
            if overall is None:
                continue
            try:
                overall = float(overall)
                local_ms = {m: float(metric_scores.get(m, 0.0)) for m in metric_names}
            except Exception:
                continue

            n_valid += 1
            sum_overall += overall
            for m in metric_names:
                sum_metric[m] += local_ms[m]

        if n_valid > 0:
            print(f"[SUMMARY] valid trajectories: {n_valid}")
            for m in metric_names:
                avg_m = sum_metric[m] / n_valid
                print(f"[SUMMARY] avg {m}: {avg_m:.4f}")
            avg_overall = sum_overall / n_valid
            print(f"[SUMMARY] avg overall: {avg_overall:.4f}")
        else:
            print("[SUMMARY] No valid judge_scores to average.")


